import math

number = 10
number *= 2  # 会改变原始值
print(number)  # number = 20
print('1 + 1 * 3 ** 3 =', 1 + 1 * 3 ** 3)  # 先做幂运算

print('10 // 11 =', 10 // 11)  # 得到整数，0.9下退至0
print('10 % 3 =', 10 % 3)  # 求余
print('3 ** 3 =', 3 ** 3)  # 求幂值
print('3 ** 3 =', 3 ** 3)  # 求幂值

print('round(1.4) =', round(1.4))
print('round(1.4) =', round(1.4))
print('abs(-1.5) =', abs(-1.5))
print('math.ceil(1.001) =', math.ceil(1.001))
print('math.floor(1.999) =', math.floor(1.999))
